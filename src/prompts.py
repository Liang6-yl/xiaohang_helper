# 读取全部data知识库（适配同目录data文件夹）
def load_school_info():
    md_files = sorted(Path("data").glob("*.md"))
    full_text = ""
    for file in md_files:
        content = file.read_text(encoding="utf-8")
        full_text += f"\n====={file.name}=====\n{content}"
    return full_text


